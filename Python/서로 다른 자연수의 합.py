T = int(input())

dp = [[0] * (2000 + 1) for _ in range(2000 + 1)]
for i in range(1, 2000 + 1):
    for a in range(1, 2000 + 1):
        if i == a == 1:
            dp[i][a] = 1
        elif a == i:
            dp[i][a] = (dp[i][a - 1] + 1) % MOD
        else:
            b = i - a
            dp[i][a] = (dp[i][a - 1] + dp[b][a - 1]) % MOD

for _ in range(T):
    N = int(input())
    print(dp[N][-1])
