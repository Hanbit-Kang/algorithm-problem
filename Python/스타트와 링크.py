import sys, itertools
input = sys.stdin.readline

N = int(input())
S = [list(map(int, input().split())) for _ in range(N)]

all = set(range(N))

cbs = list(itertools.combinations(range(N), N//2))
result = float('inf')
for i in range(len(cbs) // 2):
    a = set(cbs[i])
    b = all - a

    a_sum = 0
    for ai in a:
        for aj in a:
            a_sum += S[ai][aj]

    b_sum = 0
    for bi in b:
        for bj in b:
            b_sum += S[bi][bj]

    result = min(result, abs(a_sum - b_sum))
print(result)

